#import "CategoryListViewController.h"
#import "SpendingCategory.h"
#import "ContextNavController.h"
#import "SpendykinsAppDelegate.h"

@interface CategoryListViewController ()

@end

@implementation CategoryListViewController

- (NSManagedObjectContext *)context
{
    if (!_context) {
        SpendykinsAppDelegate *delegate = (SpendykinsAppDelegate *)[[UIApplication sharedApplication] delegate];
        _context = delegate.context;
    }
    return _context;
}

- (NSFetchedResultsController *)fetchedResultsController
{
    if (!_fetchedResultsController) {
        NSFetchRequest *fetchRequest = [[NSFetchRequest alloc] init];
        
        ContextNavController *navController = (ContextNavController *)self.navigationController;
        self.context = navController.context;
        
        NSEntityDescription *entity = [NSEntityDescription entityForName:@"SpendingCategory" inManagedObjectContext:self.context];
        [fetchRequest setEntity:entity];
        
        NSSortDescriptor *sort = [[NSSortDescriptor alloc] initWithKey:@"name" ascending:YES];
        [fetchRequest setSortDescriptors:[NSArray arrayWithObject:sort]];
        
        [fetchRequest setFetchBatchSize:20];
        
        NSFetchedResultsController *theFRC = [[NSFetchedResultsController alloc] initWithFetchRequest:fetchRequest managedObjectContext:self.context sectionNameKeyPath:nil cacheName:@"Root"];
        
        self.fetchedResultsController = theFRC;
        _fetchedResultsController.delegate = self;
    }
    return _fetchedResultsController;
}

- (void)viewDidLoad
{
    [super viewDidLoad];
    NSError *error;
    [self.fetchedResultsController performFetch:&error];

    // Uncomment the following line to preserve selection between presentations.
